#include<iostream>

using namespace std;

int main() {

	int x; // variable declaration
	cout << "enter an integer value = ";
	cin >> x;

	cout << "x = " << x << endl;

	float y;
	cout << "enter a floating point value = ";
	cin >> y;

	cout << "y = " << y << endl;

	double z;
	cout << "enter a large floating point value = ";
	cin >> z;

	cout << "z = " << z << endl;


	return 0;
}